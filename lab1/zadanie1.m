% Parametry wejœciowe
A = 3;
f = 2;
fx = f*10; % [Hz]

% Oœ pozioma (czas)
fs = 1000; % Czêst. próbkowania
% Uwaga! W spektrogramach i cwt zakres czêst. na wykresie = fs/2
t_step = 1/fs;
t = 0:t_step:2;

% Sygna³ wejœciowy
swe = A*sin(2*pi*fx*t);
%plot(t, swe); % Podgl¹d sygna³u

% Ustawienia spektrogramów
L = 128*4; % D³ugoœæ okna, im wiêksza tym lepiej widaæ moc sygna³u a gorzej czas
overlap = 120; % Musi byæ mniejsze od L
freq_res = 128*2; % Rozdzielczoœæ czêst.

% Okno prostok¹tne
figure(1);
spectrogram(swe, rectwin(L), overlap, freq_res, fs);
title('Okno prostok¹tne')

% Okno Hanna
figure(2);
spectrogram(swe, hann(L), overlap, freq_res, fs);
title('Okno Hanna')

% Okno Hamminga
figure(3);
spectrogram(swe, hamming(L), overlap, freq_res, fs);
title('Okno Hamminga')
